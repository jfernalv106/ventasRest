package com.proyecto.util;

public class Message {
	private String from;
	private String text;

	public String getFrom() {
		return from;
	}

	public void setFrom(String value) {
		this.from = value;
	}

	public String getText() {
		return text;
	}

	public void setText(String value) {
		this.text = value;
	}
}
